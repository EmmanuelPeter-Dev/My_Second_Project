function displayLGAs(selectedState) {
    lgaSelect.innerHTML = "";
    const lgas = statesAndLGAs[selectedState];
    const placeholder = document.createElement("option");
    placeholder.value = "";
    placeholder.disabled = true;
    placeholder.selected = true;
    placeholder.textContent = "Select LGA";
    lgaSelect.appendChild(placeholder);

    lgas.forEach(function (lga) {
        const option = document.createElement("option");
        option.value = lga;
        option.textContent = lga;
        lgaSelect.appendChild(option);
    });

    lgaSelect.disabled = false;
}

function displayPollingUnits(selectedLGA) {
    pollingUnitSelect.innerHTML = "";

    const units = lgaPollingUnits[selectedLGA];

    const placeholder = document.createElement("option");
    placeholder.value = "";
    placeholder.disabled = true;
    placeholder.selected = true;
    placeholder.textContent = "Select Polling Unit";
    pollingUnitSelect.appendChild(placeholder);

    units.forEach(function (unit) {
        const option = document.createElement("option");
        option.value = unit;
        option.textContent = unit;
        pollingUnitSelect.appendChild(option);
    });

    pollingUnitSelect.disabled = false;
}

stateSelect.addEventListener("change", function () {
    const selectedState = stateSelect.value;
    displayLGAs(selectedState);
});

lgaSelect.addEventListener("change", function () {
    const selectedLGA = lgaSelect.value;
    displayPollingUnits(selectedLGA);
});

